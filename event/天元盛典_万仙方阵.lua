function 事件:完成(玩家)
    if 玩家.是否组队 then
        for _, v in 玩家:遍历队伍() do
            self:掉落包(v)
        end
    else
        self:掉落包(玩家)
    end
end

function 事件:掉落包(玩家)
    if 玩家:取活动限制次数('天元盛典') >= 120 then
        玩家:常规提示('本日奖励次数已尽,无法继续获得奖励')
        return
    end
    玩家:增加活动限制次数('天元盛典')
    local 银子 = 28000
    local 经验 = 50000 * (玩家.等级 * 0.15)
    --(1+玩家.其它.鬼王次数*1.2)

    玩家:添加参战召唤兽经验(经验 * 1.5, "天元盛典")
    玩家:添加银子(银子, "天元盛典")
    玩家:添加经验(经验, "天元盛典")
    local 奖励 = 是否奖励(2007,玩家.等级,玩家.转生)
    if 奖励 ~= nil and type(奖励) == 'table' then
        local r = 生成物品 { 名称 = 奖励.道具信息.道具, 数量 = 奖励.道具信息.数量, 参数 = 奖励.道具信息.参数 }
        if r then
            玩家:添加物品({ r })
            if 奖励.道具信息.是否广播 == 1 and 奖励.广播 ~= nil then
                玩家:发送系统(奖励.广播, 玩家.名称, r.ind, r.名称)
            end
        end
    end
end

return 事件