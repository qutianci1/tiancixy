function 事件:更新()
    for k, v in pairs(_地图) do
        local map = self:取地图(v)
        local 随机 = math.random(30, 50)
        for _ = 1, 随机 do
            local X, Y = map:取随机坐标()
            local xz = math.random(#_主怪信息[k])
            local NPC =
                map:添加NPC {
                名称 = _主怪信息[k][xz].名称,
                称谓 = '天元盛典',
                外形 = _主怪信息[k][xz].模型,
                时间 = 7200,
                脚本 = 'scripts/event/天元盛典_万仙方阵.lua',
                X = X,
                Y = Y,
                事件 = self
            }
        end
    end
    self:发送系统('#Y【天元盛典】#G天庭举办天界盛典竟有妖怪混入，诸君可以三人及以上组队，速速前往#W长安东、大唐境内、五指山、长寿村外、北俱芦洲#G寻找天庭的万仙方阵捉拿妖怪。')
    return not self.是否结束 and 7200
end

